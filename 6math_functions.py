
#* math functions:
#! before we use the math functions we need to import the math library
import math

pi=3.14
number=5.5
negative=-10
#*1.round function: it will round the number to the nearest integer
print(round(pi))

#*2.floor function: it will floor the number to the lowest nearest integer
print(math.floor(number))

#*3.ceil function: it will ceil the number to the highest nearest integer
print(math.ceil(number))

#*4.abs function: it will return the absolute value of the number
print(abs(negative))

#*5.pow function: it will rise the number to a power
print(pow(number,2))

#*6.sqrt function: it will return the square value of the number
print(math.sqrt(number))

#*7.max function: it will return the maximum value of the number
print(max(number,pi))

#*8.min function: it will return the minimum value of the number
print(min(number,pi))

