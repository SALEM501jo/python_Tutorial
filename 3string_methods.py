name="salem obyat"

#1.length of the string
print(len(name))
#2.find th letter
print(name.find("o"))
#3.capitalize the first letter
print(name.capitalize())
#4.upper case
print(name.upper())
#5.lower case
print(name.lower())
#6.is a digit
print(name.isdigit())
#7.is a alphabet (if there is space between words then it is false)
print(name.isalpha())
#8.how many characters in a string
print(name.count("s"))
#9.replace the characters
print(name.replace("s","a"))
#10.display the string multiple times
print(name*3)

print(name.title())

